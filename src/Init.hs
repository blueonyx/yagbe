-- initialize ALL the Game Boy Emulation state
initGBE :: RIO App ()
initGBE = do
  Zus {..} <- get

  --logInfo $ display $ length tiles
  --logInfo $ display $ length tilemap
  
  liftIO $ do
    fillBgTileBuffer _stMemory breakoutTiles
    fillBgTileMap _stMemory breakoutTilemap
    fillMemory objTileBufferAddress _stMemory objTileBuffer
    fillMemory oamAddress _stMemory oamBuffer

-- copy bytes into the memory starting at address $9000
fillBgTileBuffer :: (Ptr CUChar) -> [CUChar] -> IO ()
fillBgTileBuffer = fillMemory bgTileBufferAddress

-- copy bytes into the memory starting at address $9800
fillBgTileMap :: (Ptr CUChar) -> [CUChar] -> IO ()
fillBgTileMap = fillMemory bgTileMapAddress

-- copy the list of bytes into the memory starting at an offset
fillMemory :: Int -> (Ptr CUChar) -> [CUChar] -> IO ()
fillMemory offset mem =
  mapM_ (\(num, byte) -> pokeByteOff mem (offset+num) byte) . zip [0..]

